let currentLang = localStorage.getItem('lang') || 'es';

export function t(key) {
  return DICTS[currentLang]?.[key] ?? DICTS.es?.[key] ?? key;
}

export function getLang() {
  return currentLang || localStorage.getItem('lang') || document.documentElement.lang || 'es';
}

export function interpolate(str, vars = {}) {
  return String(str).replace(/\{(\w+)\}/g, (_, k) => (vars[k] !== undefined ? vars[k] : `{${k}}`));
}

export function translateDom(root = document) {
  root.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (key) el.textContent = t(key);
  });

  root.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (key) el.setAttribute('placeholder', t(key));
  });

  document.documentElement.lang = currentLang;
  document.documentElement.setAttribute('data-lang', currentLang);
}

export function setLang(lang) {
  currentLang = DICTS[lang] ? lang : 'es';
  localStorage.setItem('lang', currentLang);
  translateDom();
  window.dispatchEvent(new CustomEvent('lang:changed', { detail: { lang: currentLang } }));
}

export function initI18n() {
  translateDom();
}

window.setLang = setLang;
window.initI18n = initI18n;
